
public class Logical_Opreators {
	public static void main(String[] args) {
		int value1 = 1;
		int value2 = 2;

		if ((value1 == 1) && (value2 == 2)) {
			System.out.println("Value is 1 AND value is 2");
		}

		if ((value1 == 1) || (value2 == 5)) {
			System.out.println("value1 is 1 OR value2 is 1");
		}
	}
}
